const navToggle = document.querySelector('.nav-toggle');
const navMenu = document.querySelector('.nav-menu');
const langButtons = document.querySelectorAll('.lang-btn');
const openStatus = document.querySelector('#open-status');
const reserveForm = document.querySelector('#reserve-form');
const reservationOutput = document.querySelector('#reservation-output');
let currentLang = localStorage.getItem('victoria-lang') || 'vi';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('victoria-lang', lang);
  document.documentElement.lang = lang === 'ja' ? 'ja' : lang;

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.getAttribute('data-i18n');
    const value = translations[lang]?.[key];
    if (value) node.textContent = value;
  });

  langButtons.forEach((button) => {
    button.classList.toggle('is-active', button.dataset.lang === lang);
  });

  updateOpenStatus();
}

function getHanoiHourMinute() {
  const parts = new Intl.DateTimeFormat('en-GB', {
    timeZone: 'Asia/Ho_Chi_Minh',
    hour: '2-digit',
    minute: '2-digit',
    hour12: false
  }).formatToParts(new Date());

  const hour = Number(parts.find((part) => part.type === 'hour')?.value || 0);
  const minute = Number(parts.find((part) => part.type === 'minute')?.value || 0);
  return { hour, minute };
}

function updateOpenStatus() {
  if (!openStatus) return;
  const { hour } = getHanoiHourMinute();
  const isOpen = hour >= 19 || hour < 2;
  openStatus.textContent = translations[currentLang][isOpen ? 'status.open' : 'status.closed'];
  openStatus.classList.toggle('is-open', isOpen);
}

navToggle?.addEventListener('click', () => {
  const isOpen = navMenu.classList.toggle('is-open');
  navToggle.setAttribute('aria-expanded', String(isOpen));
});

navMenu?.querySelectorAll('a').forEach((link) => {
  link.addEventListener('click', () => {
    navMenu.classList.remove('is-open');
    navToggle?.setAttribute('aria-expanded', 'false');
  });
});

langButtons.forEach((button) => {
  button.addEventListener('click', () => setLanguage(button.dataset.lang));
});

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add('is-visible');
      observer.unobserve(entry.target);
    }
  });
}, { threshold: 0.12 });

document.querySelectorAll('.reveal').forEach((node) => observer.observe(node));

reserveForm?.addEventListener('submit', async (event) => {
  event.preventDefault();
  const formData = new FormData(reserveForm);
  const message = [
    'Hello VICTORIA Bar & Lounge, I would like to reserve a table.',
    `Name: ${formData.get('name') || '-'}`,
    `Date: ${formData.get('date') || '-'}`,
    `Time: ${formData.get('time') || '-'}`,
    `Guests: ${formData.get('guests') || '-'}`,
    `Note: ${formData.get('note') || '-'}`
  ].join('\n');

  reservationOutput.hidden = false;
  reservationOutput.textContent = `${translations[currentLang]['reserve.messageTitle']}\n\n${message}\n\n${translations[currentLang]['reserve.copyHint']}`;

  try {
    await navigator.clipboard.writeText(message);
  } catch (_) {
    // Clipboard may be blocked on some browsers; the message is still visible for manual copy.
  }
});

setLanguage(currentLang);
setInterval(updateOpenStatus, 60_000);
